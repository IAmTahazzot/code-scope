#include <iostream>

int main()
{
    // Variables are memory locations that store values of a specific type
    // Declare a variable of type int
    int favorite_number = 7;
    std::cout << "My favorite number is " << favorite_number << std::endl;

    // Declare a variable of type double
    double pi = 3.14159;
    std::cout << "The value of pi is " << pi << std::endl;

    // Declare a variable of type char
    char letter = 'A';
    std::cout << "The first letter of the alphabet is " << letter << std::endl;
    return 0;
}